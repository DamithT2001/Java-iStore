    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        // TODO add your handling code here:
        String productId = txtProductId.getText().trim();
        String productName = txtProduct.getText().trim();
        String price = txtPrice.getText().trim();
        String productCat = cmbProductCat.getSelectedItem().toString();
        
        if (productId.isEmpty() || productName.isEmpty() || price.isEmpty() || productCat.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "You can't add an item with empty details!");
            // format validation
        } else if (!productId.matches("iST/P/\\d{3}")) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Product ID Format! Please use iST/P/001 format.");
            return;// 

        } else {
            if (manager1.isProductExists(productId)) {
                JOptionPane.showMessageDialog(rootPane, "Product ID already exists! Please choose a different ID.");
                return; // 
            } else {
                Products product = new Products(productId, productName, productCat, price);
                manager1.addProduct(product);
                
                JOptionPane.showMessageDialog(rootPane, "Item Added Successfully");
                manager1.viewProduct(tblStock);
            }
        }
        txtProductId.setText("");
        txtProduct.setText("");
        cmbProductCat.setSelectedItem("----Select Category----");
        txtPrice.setText("");

    }//GEN-LAST:event_btnAddActionPerformed

    private void tblStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStockMouseClicked
        //set data to text fields
        DefaultTableModel tblModel = (DefaultTableModel) tblStock.getModel();

        //set data to text field when raw is selected
        String pid = tblModel.getValueAt(tblStock.getSelectedRow(), 0).toString();
        String prod = tblModel.getValueAt(tblStock.getSelectedRow(), 1).toString();
        String cat = tblModel.getValueAt(tblStock.getSelectedRow(), 2).toString();
        String pric = tblModel.getValueAt(tblStock.getSelectedRow(), 3).toString();

        //set to input fields
        txtProductId.setText(pid);
        txtProduct.setText(prod);
        cmbProductCat.setSelectedItem(cat);
        txtPrice.setText(pric);
        btnAdd.setEnabled(false);
        txtProductId.setEnabled(false);

    }//GEN-LAST:event_tblStockMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here
        int selectedRow = tblStock.getSelectedRow();
        DefaultTableModel tblModel = (DefaultTableModel) tblStock.getModel();
        
        if (selectedRow >= 0) {
            String productId = txtProductId.getText();
            String productName = txtProduct.getText();
            String productCat = cmbProductCat.getSelectedItem().toString();
            String price = txtPrice.getText();
            String id = tblModel.getValueAt(selectedRow, 0).toString(); // Assuming the ID is in the first column

            Products product = new Products(productId, productName, productCat, price);
            manager1.updateProduct(product, id);
            JOptionPane.showMessageDialog(rootPane, "Item Updated Successfully");
            btnAdd.setEnabled(true);
            txtProductId.setText("");
            txtProduct.setText("");
            cmbProductCat.setSelectedItem("----Select Category----");
            txtPrice.setText("");
            manager1.viewProduct(tblStock);
        } else {
            // No row selected, so deactivate the update button
            btnAdd.setEnabled(true);
            JOptionPane.showMessageDialog(rootPane, "Select a row to update.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

