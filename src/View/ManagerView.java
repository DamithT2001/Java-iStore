    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblStock.getSelectedRow();
        DefaultTableModel tblModel = (DefaultTableModel) tblStock.getModel();
        
        if (selectedRow >= 0) {
            String id = (tblModel.getValueAt(selectedRow, 0).toString());
            
            int rowsDeleted = manager1.deleteProduct(id);
            if (rowsDeleted > 0) {
                // Remove the selected row from the table in the GUI
                tblModel.removeRow(selectedRow);
                
                JOptionPane.showMessageDialog(rootPane, "Item Deleted Successfully");
                btnAdd.setEnabled(true);
                txtProductId.setText("");
                txtProduct.setText("");
                cmbProductCat.setSelectedItem("----Select Category----");
                txtPrice.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed to delete the item.");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a row to delete.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

